	public static int getArrowPos(int idx, int week) {
		if (week > MAX_PAGE)
			week = week%(MAX_PAGE+1);
		return UPDATE_POS[week][idx];
	}

	public static int getPage(int score, int week) {
		if (week < 0)
			week = 0;
		else if (week > MAX_PAGE)
			week = week%(MAX_PAGE+1);
		if (score < 0)
			score = 0;
		else if (score > MAX_SCORE)
			score = MAX_SCORE;
		int idx = score * 14 / MAX_SCORE;
		return PAGE[week][idx];
	}

	public static int getMorePage(int score, int week) {
		if (week < 0)
			week = 0;
		else if (week > MAX_PAGE)
			week = week%(MAX_PAGE+1);
		if (score < 0)
			score = 0;
		else if (score > MAX_SCORE)
			score = MAX_SCORE;
		int idx = score * 14 / MAX_SCORE;
		return MORE_PAGE[week][idx];
	}

	public static int getPageByIdx(int idx, int week) {
		if (week < 0)
			week = 0;
		else if (week > MAX_PAGE)
			week = week%(MAX_PAGE+1);
		if (idx < 0)
			idx = 0;
		else if (idx > 14)
			idx = 14;
		return PAGE[week][idx];
	}

	public static int getPageIdx(int score, int week) {
		if (week < 0)
			week = 0;
		else if (week > MAX_PAGE)
			week = week%(MAX_PAGE+1);
		if (score < 0)
			score = 0;
		else if (score > MAX_SCORE)
			score = MAX_SCORE;
		int idx = score * 14 / MAX_SCORE;
		return idx;
	}

	public static int[] getAnimationBgs(Integer[] scores) {
		if (scores == null)
			return null;
		int[] pages = new int[scores.length];
		for (int i = 0; i < pages.length; ++i)
			pages[i] = getPage(scores[i], i);
		return pages;
	}

	public static int[] getMoreAnimationBgs(Integer[] scores) {
		if (scores == null)
			return null;
		int[] pages = new int[scores.length];
		for (int i = 0; i < pages.length; ++i)
			pages[i] = getMorePage(scores[i], i);
		return pages;
	}

